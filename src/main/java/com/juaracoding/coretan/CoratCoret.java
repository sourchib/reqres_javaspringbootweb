package com.juaracoding.coretan;

public class CoratCoret {

    public Double tambah(Integer x , Integer y){
        return (double) (x+y);
    }

    public Double kurang(Integer x , Integer y){
        return (double) (x-y);
    }

    public Double kali(Integer x , Integer y){
        return (double) (x*y);
    }
}
